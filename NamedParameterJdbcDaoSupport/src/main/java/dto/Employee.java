package dto;

public class Employee {
	private int eid;
	private String ename;
	private float esallary;
	private String ecity;
	
	public int getEid() {
		return eid;
	}
	public void setEid(int eid) {
		this.eid = eid;
	}
	public String getEname() {
		return ename;
	}
	public void setEname(String ename) {
		this.ename = ename;
	}
	public float getEsallary() {
		return esallary;
	}
	public void setEsallary(float esallary) {
		this.esallary = esallary;
	}
	public String getEcity() {
		return ecity;
	}
	public void setEcity(String ecity) {
		this.ecity = ecity;
	}
	
	
	
}
